					-- Q2. Create all six tables with appropriate data types and constraints.
					-- Q3. Include all necessary PRIMARY KEY and FOREIGN KEY relationships.
					-- Q4. Add UNIQUE and NOT NULL where needed.*/

create table Authors_Table (
Author_ID varchar (15) PRIMARY KEY not null,
Author_Name varchar (100),
Country varchar (50)
);

create table Books_Table (
Book_id varchar (15),
Book_name varchar (100),
Author_id varchar (15),
category varchar (100),
Price decimal (10,2),
Stock int,
primary key (book_id)
);

create table customers (
customer_id varchar (15) primary key not null,
customer_name varchar (100),
email varchar (100),
sex enum ('Male', 'Female'),
country varchar (50)
);

create table sales(
sales_id varchar (15) primary key not null,
customer_id varchar (15), 
customer_name varchar (100),
Book_id varchar (15), 
Book_name varchar (100),
Quantity int,
price decimal (10,2),
Total_amount int,
Sales_Date date);

create table category (
category_id varchar (15) primary key not null,
category_name varchar (100)
);

create table transactions (
transaction_id varchar (15) not null primary key,
author_id varchar (15),
book_id varchar (15),
customer_id varchar (15),
sales_id varchar (15),
category_id varchar (15),
customer_name varchar (100),
payment_method varchar(100),
payment_status varchar (100),
transaction_date date,
foreign key (author_id) references authors_table(author_ID),
foreign key (book_id) references books_table(book_id),
foreign key (customer_id) references customers(customer_id),
foreign key (sales_id) references sales(sales_id),
foreign key (category_id) references category(category_id)
);


